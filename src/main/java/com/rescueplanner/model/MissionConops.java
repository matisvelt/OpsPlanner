package com.rescueplanner.model;

public class MissionConops {
  private String missionStatement;
  private String conopsPhase0;
  private String conopsPhase1;
  private String conopsPhase2;
  private String conopsPhase3;
  private String timeline;
  private String draft;

  public MissionConops() {
  }

  public String getMissionStatement() {
    return missionStatement;
  }

  public void setMissionStatement(String missionStatement) {
    this.missionStatement = missionStatement;
  }

  public String getConopsPhase0() {
    return conopsPhase0;
  }

  public void setConopsPhase0(String conopsPhase0) {
    this.conopsPhase0 = conopsPhase0;
  }

  public String getConopsPhase1() {
    return conopsPhase1;
  }

  public void setConopsPhase1(String conopsPhase1) {
    this.conopsPhase1 = conopsPhase1;
  }

  public String getConopsPhase2() {
    return conopsPhase2;
  }

  public void setConopsPhase2(String conopsPhase2) {
    this.conopsPhase2 = conopsPhase2;
  }

  public String getConopsPhase3() {
    return conopsPhase3;
  }

  public void setConopsPhase3(String conopsPhase3) {
    this.conopsPhase3 = conopsPhase3;
  }

  public String getTimeline() {
    return timeline;
  }

  public void setTimeline(String timeline) {
    this.timeline = timeline;
  }

  public String getDraft() {
    return draft;
  }

  public void setDraft(String draft) {
    this.draft = draft;
  }
}
